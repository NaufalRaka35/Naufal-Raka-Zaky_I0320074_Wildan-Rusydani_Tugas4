x = 4									#4 = 0100
y = 11									#11 = 1011
z = 0

#output dari 4 | 11
z = x | y
print("Hasil dari 4 | 11 adalah", z)

#output dari 4 >> 11
z = x >> y
print("Hasil dari 4 >> 11 adalah", z)

#output dari 4 ^ 11
z = x ^ y
print("Hasil dari 4 ^ 11 adalah", z)

#output dari ~4
z = ~x
print("Hasil dari ~4 adalah", z)

#output dari 11&4
z = y & x
print("Hasil dari 11 & 4 adalah", z)